                        <h2>6 Rekomendasi Desain Grafis Android</h2></a>
        <h5>04 November 2022 Oleh <a href="#" target="_blank">Iska</a></h5>
        <img src="../img/desain-grafis-android.png" class="thumbnail" style="height:100%;">
        <p>Anda kini sudah bisa menggunakan aplikasi desain grafis Android yang ada di ponsel. Kegiatan ini cukup dilakukan memakai smartphone tanpa harus melakukan instalasi di PC seperti laptop atau komputer. Bahkan,  pngguna dapat meningkatkan kemampuan editing-nya guna membuat hasil menakjubkan.</p>
        <h2>Beberapa Pilihan Desain Grafis Android</h2>
        <p>Seiring berkembangnya zaman, aplikasi desain grafis yang bisa digunakan di Android memang semakin mudah diakses. Anda tidak perlu repot menginstall atau membuka perangkat besar lainnya seperti laptop. Pasalnya kini sudah tersedia beragam tools cukup menggunakan beberapa aplikasi berikut: </p>
        <h3>1.	PicsArt</h3>
        <p>PicsArt menjadi salah satu editor foto terbaik yang bisa digunakan melalui smartphone. Nyatanya pengguna juga bisa menggunakannya untuk menggambar atau bahkan membuat desain grafis termasuk berbentuk 3D melalui banyaknya fitur di dalamnya.</p>
        <p>Misalnya bisa menggunakan fitur berupa brush tools untuk membuat foto terlihat lebih artistic. Tidak hanya itu saja, pengguna dapat mengatur tingkat kecerahan, color, opacity sampai blend. Menariknya lagi, apabila bingung tidak memiliki inspirasi maka lihatlah karya orang lain di tampilan utama.</p>
        <h3>2.	Canva</h3>
        <p>Anda juga bisa memanfaatkan fitur di dalam Canva untuk membuat desain grafis. Para user dapat menggunakannya dalam mendesain berbagai bentuk mulai dari ucapan selamat ulang tahun, membuat logo, flyer hingga label bagi sebuah kemasan produk.</p>
        <p>Ada dua pilihan yang bisa dipilih untuk menggunakan Canva ini yakni dengan membuatnya sendiri dari awal atau memanfaatkan template berjumlah 6000 lebih di sini. Aplikasi ini juga menyediakan berbagai macam tools cukup lengkap bagi pengguna agar memudahkan kebutuhan desain mereka.</p>
        <h3>3.	Adobe Photoshop Express</h3>
        <p>Berbeda dengan sebelumnya, aplikasi ini memang dirancang khusus untuk memenuhi kebutuhan desain grafis bagi penggunanya. Kebanyakan orang pasti tidak asing lagi dengan Adobe Photoshop Express yang hanya bisa digunakan melalui PC, namun kini sudah dapat diakses memakai smartphone.</p>
        <p>Perbedaan kualitas daripada aplikasi sebelumnya yakni hadir dari gambar yang dihasilkan degan resolusi tinggi. Pengguna pun bisa memanfaatkan beragam fitur dan filter cukup lengkap di Adobe Photoshop Express. User dapat memakai format foto mulai dari JPG, PNG, arw, cr2, dng dan lainnya.</p>
        <h3>4.	AutoCAD</h3>
        <p>AutoCAD menjadi pilihan terbaik bagi Anda yang memiliki hobi menggambar. Pasalnya aplikasi ini memiliki fitur untuk memudahkan penggunanya dalam membuat desain maupun gambar sesuai keinginan mereka. Salah satunya yaitu mampu melihat setiap koordinat agar terlihat lebih detail.</p>
        <p>Anda juga bisa menyimpan hasil karya setelah menggunakan AutoCAD berupa PDF atau DWF. Biasanya banyak orang menggunakan aplikasi ini untuk membuat gambar yang memerlukan banyak detail seperti desain rumah. Fitur di dalamnya memang diakui sangat efektif menunjang kreatifitas.</p>
        <h3>5.	Adobe Photoshop Lightroom CC</h3>
        <p>Sama seperti Adobe Photoshop Express, aplikasi ini berada di bawah naungan Adobe yakni Adobe Photoshop Lightroom CC. Pengguna bisa memilikinya jika ingin fokus dalam meningkatkan kualitas foto dengan memanfaatkan beragam fitur menarik yang ada di sini.</p>
        <p>Anda juga bisa melakukan editing atau mengatur tingkat kecerahan pada foto dan menjadikannya agar lebih terlihat menarik lagi. Bahkan, tidak akan merasa kebingungan untuk menggunakannya sebab desain dari tampilan antar mukanya sudah dirancang cukup sederhana.</p>
        <h3>6.	Adobe Illustrator Draw</h3>
        <p>Aplikasi ini juga masih dikembangkan oleh Adobe. Adobe Illustrator Draw cocok bagi Anda yang memiliki ketertarikan dalam dunia menggambar dan ingin langsung mengeditnya. Desainer grafis pemula pun bisa memanfaatkan kemudahan fitur di dalamnya untuk mengasah hobi dan kemampuan.</p>
        <p>Di dalamnya juga tersedia fitur penunjang lain yang tidak kalah menarik, yaitu pengguna dapat melakukan zoom pada layar kerja hingga mencapai 64 kali perbesaran. Tidak hanya itu saja, Adobe Illustrator Draw juga menyediakan layer agar user lebih mudah ketika menggambar.</p>
        <p>Keenam rekomendasi desain grafis Android di atas bisa Anda pilih untuk mulai mendesain cukup dengan menggunakan smartphone saja. Tentunya hal ini cukup memudahkan bagi pengguna yang ingin mengasah hobi mereka. Poin pentingnya ialah aplikasi ini menyediakan fitur lengkap di dalamnya.</p>
